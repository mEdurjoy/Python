a = "Durjoy"
print(a[:])
print(a[1:4])
print(a[2:])
print(a[:5])
print(a[::3])
print(a[::-1])

print(a[1:5:2])  # Slicing with step
print(a[::2])    # Slicing with step of 2
print(a[1:5:3])  # Slicing with step of 3
print(a[::])     # Full slice
print(a[1:])    # Slicing from index 1 to end
print(a[:4])    # Slicing from start to index 4
print(a[1:4:2])  # Slicing with step of 2
print(a[::-2])  # Slicing with step of -2 (reverse)